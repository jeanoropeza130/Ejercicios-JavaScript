//Calcular el Valor Absoluto de un Número con la Función Math.abs

console.log(Math.abs(-1));
console.log(Math.abs(1));
console.log(Math.abs(0));
console.log(Math.abs(-0));
console.log(Math.abs(0.1));
console.log(Math.abs(-0.1));
console.log(Math.abs(0.1 + 0.2));
console.log(Math.abs(-0.1 + 0.2));
console.log(Math.abs(0.1 - 0.2));

console.log();
console.log(Math.abs(null));
console.log(Math.abs(undefined));
console.log(Math.abs('-1'));
console.log(Math.abs('1'));